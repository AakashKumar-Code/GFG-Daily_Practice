class Solution
{
    public:
    Node* insertionSort(struct Node* head)
    {
        //code here
        if(head==NULL || head->next==NULL) return head;
        Node* d=new Node(-1);
        d->next=head;
        Node* node=head->next, *prev=head;
        while(node){
            if(node->data>=prev->data){
                prev=node;
                node=node->next;
                continue;
            }
            Node* temp=d;
            while(node->data>temp->next->data){
                temp=temp->next;
            }
            prev->next=node->next;
            node->next=temp->next;
            temp->next=node;
            node=prev->next;
        }
        return d->next;
    }
    
};

